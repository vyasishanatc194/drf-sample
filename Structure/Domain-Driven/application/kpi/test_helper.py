        kpi_frequencies = []
        for i in range(number):
            kpi_frequency = (
                kpi_frequency_services.get_kpi_frequency_factory().build_entity_with_id(
                    kpi_id=KPIID(value=kpi_obj.id),
                    calender_manager_id=CalenderManagerID(value=calendar_manager.id),
                    frequency=KPI.MONTHLY,
                    daily_data=daily_frequency[0],
                    weekly_data=weekly_frequency[0],
                    monthly_data=monthly_frequency[0],
                    yearly_data=yearly_frequency[0],
                    quarterly_data=quarterly_frequency[0],
                )
            )
            kpi_frequencies.append(kpi_frequency)
        return kpi_frequencies

